package model;

/**
 *
 * class Item = POJO - Plain Object Java Object
 */
public class Item {
    
    // attributes
    private String name;
    private String type;
    private int price;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }
}
